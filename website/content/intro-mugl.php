	<p class="homepagetext">
		arkLogic User Group London is an organization for people
		that are users or fans of MarkLogic and those who are interested in
		learning more about MarkLogic, XML, XQuery and related technologies.
	</p>
	<p class="homepagetext">
		While MarkLogic is where our common interest begins, the members
		of MUGL share many interests other in the "X" technologies (XML/XSL/XQuery)
		as well as web application design, publishing systems, search
		optimization and other content-centric disciplines.
	</p>
	<p class="homepagetext">
		All of our regular meetings are free and open to the public.
		There is no formal membership requirement and all are welcome.
        To reserve a spot at the next meeting, visit the MeetUp link on
        the right side of this page.
	</p>
	<p class="homepagetext">
		We meet approximately every other month.  Sometimes we break the
        regular pattern if someone special is visting from out of town and we
        can snag them to speak to the group.  If you're someone special and
        will be visiting London in the next few weeks and would like to be
        a speaker, please get in touch: mugl@mugl.org.
	</p>

