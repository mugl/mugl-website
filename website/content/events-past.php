    <h2>November 19, 2011: The Prague Autumn</h2>
    <div class="floatpicture"><img src="images/JimFuller.png" width="110" height="110"/></div>

    <p class="homepagetext">
        On Thursday, November 19, 2011 we had a very interesting presentation
        by <a href="http://webcomposite.com/" target="kay">James Fuller</a>
        of MarkLogic.  Jim is an XQuery innovator and spoke about
        being more productive with
        <i>XQuery - The GSD Language</i>.  If you've never heard of the MarkLogic
        term "GSD", it's like Getting Things Done, but a bit ruder.
    </p>
    <p class="homepagetext">
        The slides for Jim's presentation from the GoTo Conference are available on
        <a href="http://www.slideshare.net/jimfuller2009/xquery-the-gsd-getting-stuff-done-language" target="slideshare">SlideShare</a>
    </p>


	<h2>October 19, 2011: The Saxon Invasion</h2>
	<div class="floatpicture"><img src="images/225px-Michael_kay_nov_2008.jpg" width="110" height="128"/></div>

	<p class="homepagetext">
	On Wednesday, October 19, 2011 we had the pleasure of hosting
	<a href="http://saxonica.com/contact/contact.xml" target="kay">Dr. Michael Kay</a>,
	the father of <a href="http://saxonica.com" target="kay">Saxon</a>
	and influential member of various and sundry XML-related W3C standards committees.
	</p>

	<p class="homepagetext">
	Dr. Kay gave us a very interesting introduction to his Saxon-in-the-browser
	XSLT 2.0 processor that's implemented with JavaScript and GWT.  If you missed
	this talk you've missed one of the great innovators of the XML world.
	</p>


