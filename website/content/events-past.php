<h2>July 10: The (Cyber)Man From The BBC<sup style="color: #bbc">*</sup></h2>
<div class="floatpicture"><img src="images/jem_rayfield.png" alt="Jem Rayfield" title="Jem Rayfield"/></div>

<p class="homepagetext">
